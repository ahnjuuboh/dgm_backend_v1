package com.betimes.betext.json;

public class SourceJson {
    private String source_id;
    private String source_type;
    private String source_name;
    private String source_username;
    private String source_image;
    private String source_link;
    private String source_category;
    private String source_fans;

    public String getSource_id() {
        return source_id;
    }

    public void setSource_id(String source_id) {
        this.source_id = source_id;
    }

    public String getSource_type() {
        return source_type;
    }

    public void setSource_type(String source_type) {
        this.source_type = source_type;
    }

    public String getSource_name() {
        return source_name;
    }

    public void setSource_name(String source_name) {
        this.source_name = source_name;
    }

    public String getSource_username() {
        return source_username;
    }

    public void setSource_username(String source_username) {
        this.source_username = source_username;
    }

    public String getSource_image() {
        return source_image;
    }

    public void setSource_image(String source_image) {
        this.source_image = source_image;
    }

    public String getSource_link() {
        return source_link;
    }

    public void setSource_link(String source_link) {
        this.source_link = source_link;
    }

    public String getSource_category() {
        return source_category;
    }

    public void setSource_category(String source_category) {
        this.source_category = source_category;
    }

    public String getSource_fans() {
        return source_fans;
    }

    public void setSource_fans(String source_fans) {
        this.source_fans = source_fans;
    }
}
